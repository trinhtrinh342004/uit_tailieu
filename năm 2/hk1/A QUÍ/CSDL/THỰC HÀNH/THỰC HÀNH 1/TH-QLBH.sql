CREATE DATABASE QLBHLAB2
GO

USE QLBHLAB2

CREATE TABLE KHACHHANG
(	MAKH char(4) PRIMARY KEY,
	HOTEN varchar(40),
	DCHI varchar(50),
	SODT varchar(20),
	NGSINH smalldatetime,
	DOANHSO money,
	NGDK smalldatetime
)
GO

CREATE TABLE NHANVIEN
(	MANV char(4) PRIMARY KEY,
	HOTEN varchar(40),
	SODT varchar(20),
	NGVL smalldatetime
)
GO

CREATE TABLE SANPHAM
(	MASP char(4) PRIMARY KEY,
	TENSP varchar(40),
	DVT varchar(20),
	NUOCSX varchar(40),
	GIA money
)
GO

CREATE TABLE HOADON
(	SOHD int PRIMARY KEY,
	NGHD smalldatetime,
	MAKH char(4),
	MANV char(4),
	TRIGIA money,

	FOREIGN KEY (MAKH) REFERENCES dbo.KHACHHANG(MAKH),
	FOREIGN KEY (MANV) REFERENCES dbo.NHANVIEN(MANV)
)
GO

CREATE TABLE CTHD
(	SOHD int,
	MASP char(4),
	SL int,

	CONSTRAINT FK1_CTHD FOREIGN KEY (SOHD) REFERENCES dbo.HOADON(SOHD),
	CONSTRAINT FK2_CTHD FOREIGN KEY (MASP) REFERENCES dbo.SANPHAM(MASP),
	CONSTRAINT PK_CTHD PRIMARY KEY (SOHD,MASP)
)
GO

